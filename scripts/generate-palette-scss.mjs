/**
 * Generates scss/abstracts/_palettes.generated.scss from tokens/colors.css
 */
import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const css = readFileSync(join(root, "tokens/colors.css"), "utf8");

function extractBlock(source, startMarker, endMarker) {
  const start = source.indexOf(startMarker);
  const end = source.indexOf(endMarker, start);
  return source.slice(start, end);
}

function parseVars(block) {
  const map = {};
  for (const m of block.matchAll(/--([a-z0-9-]+):\s*([^;]+);/gi)) {
    map[m[1]] = m[2].trim();
  }
  return map;
}

const oklchBlock = extractBlock(css, ":root {", "/* Repli hex");
const hexBlock = extractBlock(css, "@supports not", "}");

const oklch = parseVars(oklchBlock);
const hex = parseVars(hexBlock);

function groupByPalette(vars) {
  const palettes = {};
  for (const [key, value] of Object.entries(vars)) {
    if (key.startsWith("color-")) continue;
    const match = key.match(/^([a-z]+)-(.+)$/);
    if (!match) continue;
    const [, name, step] = match;
    if (step === "default") continue;
    if (!palettes[name]) palettes[name] = {};
    palettes[name][step] = value;
  }
  return palettes;
}

function toSassMap(palettes) {
  const names = Object.keys(palettes).sort();
  let out = "(\n";
  for (const name of names) {
    const steps = Object.keys(palettes[name]).sort((a, b) => {
      const na = Number(a);
      const nb = Number(b);
      if (!Number.isNaN(na) && !Number.isNaN(nb)) return na - nb;
      return a.localeCompare(b);
    });
    out += `  ${name}: (\n`;
    for (const step of steps) {
      const v = palettes[name][step];
      out += `    ${step}: ${v},\n`;
    }
    out += `  ),\n`;
  }
  out += ")";
  return out;
}

const oklchPalettes = groupByPalette(oklch);
const hexPalettes = groupByPalette(hex);

const scss = `// Generated by npm run generate:palettes — do not edit by hand
// Source: tokens/colors.css (PimentCSS v1)

$palettes-oklch: ${toSassMap(oklchPalettes)};

$palettes-hex: ${toSassMap(hexPalettes)};
`;

const outDir = join(root, "scss", "abstracts");
mkdirSync(outDir, { recursive: true });
const outPath = join(outDir, "_palettes.generated.scss");
writeFileSync(outPath, scss);
console.log("✓", outPath);

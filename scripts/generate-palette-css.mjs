/**
 * Generates styles/palettes.css from tokens/colors.css
 */
import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { PALETTES } from "./palette-config.mjs";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const palettes = PALETTES;

function textColor(paletteId, step) {
  if (paletteId === "shades") {
    return step === "white" ? "var(--neutral-900)" : "var(--neutral-100)";
  }
  const n = Number(step);
  if (!Number.isNaN(n) && n <= 300) return `var(--${paletteId}-900)`;
  if (step === "450") return `var(--${paletteId}-900)`;
  return `var(--${paletteId}-100)`;
}

function chipVar(paletteId, step) {
  if (paletteId === "shades") return `var(--neutral-${step})`;
  return `var(--${paletteId}-${step})`;
}


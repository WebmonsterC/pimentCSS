/**
 * Generates @font-face (SCSS + plain CSS) and scss/abstracts/_fonts.generated.scss
 */
import { existsSync, writeFileSync, mkdirSync, copyFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';
import { FONTS_DIR, REQUIRED_FONTS } from './fonts-config.mjs';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const fontsDir = join(root, FONTS_DIR);
const stylesDir = join(root, 'styles');
const scssTokensDir = join(root, 'scss', 'tokens');
const scssAbstractDir = join(root, 'scss', 'abstracts');
const distFontsDir = join(root, 'dist', 'fonts');

mkdirSync(stylesDir, { recursive: true });
mkdirSync(scssTokensDir, { recursive: true });
mkdirSync(scssAbstractDir, { recursive: true });

const available = REQUIRED_FONTS.every((f) => existsSync(join(fontsDir, `${f.fileBase}.woff2`)));

if (!available) {
  writeFileSync(join(stylesDir, 'fonts.local.css'), '/* Run: npm run build:fonts */\n', 'utf8');
  writeFileSync(
    join(scssTokensDir, '_fonts-local.generated.scss'),
    '// Run: npm run build:fonts\n',
    'utf8',
  );
  writeFileSync(
    join(scssAbstractDir, '_fonts.generated.scss'),
    '$fonts-local-available: false !default;\n',
    'utf8',
  );
  console.warn('⚠ fonts/ incomplete — CDN fallbacks will be used (npm run build:fonts)');
  process.exit(0);
}

/** Paths relative to dist/pimentcss.css and styles/fonts.local.css */
const distUrl = (file) => `../${FONTS_DIR}/${file}`;
const stylesUrl = (file) => `../${FONTS_DIR}/${file}`;

function faceBlock(font, urlFn) {
  const file = `${font.fileBase}.woff2`;
  return `@font-face {
  font-family: "${font.family}";
  font-style: ${font.style};
  font-weight: ${font.weight};
  font-display: swap;
  src: url("${urlFn(file)}") format("woff2");
}`;
}

const facesScss = REQUIRED_FONTS.map((f) => faceBlock(f, distUrl)).join('\n\n');
const facesCss = REQUIRED_FONTS.map((f) => faceBlock(f, stylesUrl)).join('\n\n');

writeFileSync(
  join(scssTokensDir, '_fonts-local.generated.scss'),
  `// Generated by npm run build:fonts — do not edit by hand
${facesScss}
`,
  'utf8',
);

writeFileSync(
  join(stylesDir, 'fonts.local.css'),
  `/* Generated by npm run build:fonts — do not edit by hand */
${facesCss}
`,
  'utf8',
);

writeFileSync(
  join(scssAbstractDir, '_fonts.generated.scss'),
  `// Generated by npm run build:fonts — do not edit by hand
$fonts-local-available: true !default;
`,
  'utf8',
);

mkdirSync(distFontsDir, { recursive: true });
for (const font of REQUIRED_FONTS) {
  copyFileSync(
    join(fontsDir, `${font.fileBase}.woff2`),
    join(distFontsDir, `${font.fileBase}.woff2`),
  );
}

console.log('✓ scss/tokens/_fonts-local.generated.scss');
console.log('✓ styles/fonts.local.css');
console.log('✓ dist/fonts/*.woff2');
console.log('✓ scss/abstracts/_fonts.generated.scss');
